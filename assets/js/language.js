/// ==============================
// Current language (Default: English)
// ==============================
let currentLanguage = sessionStorage.getItem('language') || 'en';


// ==============================
// Initialize language
// ==============================
function initLanguage() {
    setLanguage(currentLanguage);
}


// ==============================
// Set language
// ==============================
function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';

    currentLanguage = lang;
    sessionStorage.setItem('language', lang);

    updatePageLanguage();
    updateLanguageSelector();
    updateDynamicContent();
}


// ==============================
// Update page language
// ==============================
function updatePageLanguage() {
    const langPack = translations[currentLanguage] || {};
    const enPack = translations.en || {};

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (!key) return;

        let text = '';

        // ✅ FIX CHUẨN:
        // - cho phép chuỗi rỗng ""
        // - không fallback sai sang tiếng khác
        if (key in langPack) {
            text = langPack[key];
        } else if (key in enPack) {
            text = enPack[key];
        }

        if (!text) return;

        // ==============================
        // 🔥 replace dynamic variables
        // ==============================
        Object.keys(el.dataset).forEach(attr => {
            if (attr === 'i18n') return;

            let value = el.dataset[attr];

            // format number
            if (!isNaN(value)) {
                value = Number(value).toLocaleString(
                    currentLanguage === 'vi' ? 'vi-VN' : 'en-US'
                );
            }

            // {{rate}}, {{fee}}, {{percent}}...
            text = text.replaceAll(`{{${attr}}}`, value);
        });

        // ==============================
        // apply text
        // ==============================
        if (
            el.tagName === 'INPUT' ||
            el.tagName === 'TEXTAREA'
        ) {
            el.placeholder = text;
        } else if (el.tagName === 'BUTTON') {
            el.textContent = text;
            el.value = text;
        } else {
            el.textContent = text;
        }
    });

    // Update <html lang="">
    document.documentElement.lang = currentLanguage;
}


// ==============================
// Language selector UI
// ==============================
function updateLanguageSelector() {
    const langMap = {
        en: { flag: 'gb', name: 'English' },
        vi: { flag: 'vn', name: 'Tiếng Việt' },
        ja: { flag: 'jp', name: '日本語' }
    };

    const info = langMap[currentLanguage];
    if (!info) return;

    document.querySelectorAll('[data-lang-selector]').forEach(btn => {
        const img = btn.querySelector('img');
        const span = btn.querySelector('span');

        if (img) {
            img.src = `https://flagcdn.com/24x18/${info.flag}.png`;
            img.alt = info.name;
        }

        if (span) {
            span.textContent = info.name;
        }
    });
}


// ==============================
// Translation helper
// ==============================
function t(key) {
    const langPack = translations[currentLanguage] || {};
    const enPack = translations.en || {};

    if (key in langPack) return langPack[key];
    if (key in enPack) return enPack[key];
    return '';
}


